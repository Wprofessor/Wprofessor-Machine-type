#include <bits/stdc++.h>
using namespace std;
int main()
{
    double n;
    while (cin >> n)
    {
        if (n <= 3500)
        {
            cout << n << endl;
        }
        else
        {
            if (n >= (25000 * 0.35 + 20000 * 0.3 + 26000 * 0.25 + 4500 * 0.2 + 300 + 1500 * 0.03) + 3500)
            {
                n = (n - ((25000 * 0.35 + 20000 * 0.3 + 26000 * 0.25 + 4500 * 0.2 + 300 + 1500 * 0.03) + 3500)) / 0.55 + 83500;
            }
            else if (n > 44755)
            {
                n = (n - 44755) / 0.65 + 58500;
            }
            else if (n > 30755)
            {
                n = (n - 30755) / 0.7 + 38500;
            }
            else if (n > 11255)
            {
                n = (n - 11255) / 0.75 + 12500;
            }
            else if (n > 7655)
            {
                n = (n - 7655) / 0.8 + 8000;
            }
            else if (n > 4955)
            {
                n = (n - 4955) / 0.9 + 5000;
            }
            else if (n > 3500)
            {
                n = (n - 3500) / 0.97 + 3500;
            }

            n = (int)n;
            cout << n << endl;
        }
    }
    return 0;
}